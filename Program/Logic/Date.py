# coding=utf-8

class Date:

    def __init__(self, date_id, day_first, month_first, year_first, hour_first,
                 day_second, month_second, year_second, hour_second):
        self.date_id = date_id
        self.day_first = day_first
        self.month_first = month_first
        self.year_first = year_first
        self.hour_first = hour_first
        self.day_second = day_second
        self.month_second = month_second
        self.year_second = year_second
        self.hour_second = hour_second
